//Q.3 - Write a program to check if the two numbers 23 and 45 are equal.

public class nocomparision {
   public static void main(String[] args) {
      int num1 = 23;
      int num2 = 45;
      
      if (num1 == num2) {
         System.out.println("The numbers " + num1 + " and " + num2 + " are equal.");
      } else {
         System.out.println("The numbers " + num1 + " and " + num2 + " are not equal.");
      }
   }
}
