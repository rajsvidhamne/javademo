//Q.4 - Write a program to print the power of 7 raised to 5.
public class powerofseven {
   
      public static void main(String[] args) {
          // Calculate the power of 7 raised to 5
         double result = Math.pow(7, 5);

          // Print the result
         System.out.println("7 raised to the power of 5 is: " + result);
      }
   }

