public class nooperations {
      public static void main(String[] args) {
          // Initial number
         int number = 2345;

          // Add 8
         int added = number + 8;

          // Divide by 3
         int quotient = added / 3;  
          // Take modulus with 5
         int modulus = quotient % 5;

          // Multiply by 5
          int result = modulus * 5;

          // Display the final result
         System.out.println("Final result: " + result);
      }
}
