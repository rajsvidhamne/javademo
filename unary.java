public class unary {
   public static void main(String[] args) {
      int a =10;
      int b =10;
      System.out.println("Post increment:" + a++);
      System.out.println(a);
      System.out.println("Pre increment:" + ++a);
      System.out.println(a);
      System.out.println("Pre decrement:" + b--);
      System.out.println(b);
      System.out.println("Pre decrement:" + --b);
      System.out.println(b);
   }
   
}
